import (
	"fmt"
	"github.com/davidlukac/go-tableau-cli/internal"
	log "github.com/sirupsen/logrus"
	"os"

	"github.com/spf13/cobra"
	"github.com/spf13/viper"
)

// rootCmd represents the base command when called without any subcommands
var (
	cfgFile    string
	outputFlag string
	rootCmd    = &cobra.Command{
		Use:    "go-tableau-cli",
		Short:  "Tableau Server CLI",
		PreRun: internal.LoggingSetup,
	}
)

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

func init() {
	cobra.OnInitialize(initConfig)

	// rootCmd.PersistentFlags().StringVar(&cfgFile, "config", "", "config file (default is $HOME/.go-tableau-cli.yaml)")

	rootCmd.PersistentFlags().StringVarP(&outputFlag, "output", "o", "text", "Output format")

	rootCmd.Flags().BoolP("toggle", "t", false, "Help message for toggle")
}

func initConfig() {
	if cfgFile != "" {
		// Use config file from the flag.
		viper.SetConfigFile(cfgFile)
	} else {
		// Find home directory.
		home, err := os.UserHomeDir()
		cobra.CheckErr(err)

		// Search config in home directory with name ".tableau-cli" (without extension).
		viper.AddConfigPath(home)
		viper.SetConfigName(".tableau-cli")
		viper.SetConfigType("yaml")
	}

	_, err := internal.LoadConfig(".")
	if err != nil {
		log.Fatal("cannot load .local.env config:", err)
	}

	viper.AutomaticEnv()

	if err := viper.ReadInConfig(); err == nil {
		fmt.Println("Using config file:", viper.ConfigFileUsed())
	}
}
